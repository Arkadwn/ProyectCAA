public class ExperienciaEducativa {
    private String nombreEE;
    private String creditos;
    private String idExperEdu;
    private String idIdioma;

    public String getNombreEE() {
        return nombreEE;
    }

    public void setNombreEE(String nombreEE) {
        this.nombreEE = nombreEE;
    }

    public String getCreditos() {
        return creditos;
    }

    public void setCreditos(String creditos) {
        this.creditos = creditos;
    }

    public String getIdExperEdu() {
        return idExperEdu;
    }

    public void setIdExperEdu(String idExperEdu) {
        this.idExperEdu = idExperEdu;
    }

    public String getIdioma() {
        return idIdioma;
    }

    public void setIdioma(String idIdioma) {
        this.idIdioma = idIdioma;
    }
    
    
}
